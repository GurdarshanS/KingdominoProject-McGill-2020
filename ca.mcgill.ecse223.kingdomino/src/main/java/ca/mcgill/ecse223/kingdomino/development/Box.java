package ca.mcgill.ecse223.kingdomino.development;

public class Box {
	
		private int width;
		private int height;
		
		public Box(int width, int height) {
			this.width=width;
			this.height=height;
		}
		
		public int getWidth() {return this.width;}
		public int getHeight() {return this.height;}
		
	
}
